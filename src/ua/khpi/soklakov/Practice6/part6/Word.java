package ua.khpi.soklakov.Practice6.part6;

/**
 * Represents word.
 * 
 * @author Oleg Solakov.
 *
 */
public class Word {

	/**
	 * Name word.
	 */
	private String name;

	/**
	 * Constructor class Word. Set value to field name.
	 * 
	 * @param name
	 *            specified word.
	 */
	public Word(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}
}
